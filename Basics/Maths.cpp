#include<iostream>
using namespace std;
int main(){
	
	int num1, num2;
	
	cout << "Type a number: ";
	cin >> num1;
	
	cout << "Type another number: ";
	cin >> num2;
	
	cout << num1 << " + " << num2 << " = " << num1 + num2 << endl;
	cout << num1 << " - " << num2 << " = " << num1 - num2 << endl;
	cout << num1 << " * " << num2 << " = " << num1 * num2 << endl;
	cout << num1 << " / " << num2 << " = " << num1 / num2 << endl;
}
